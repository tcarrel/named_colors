std::ostream& operator<<( std::ostream& os, const std::tuple<double, double, double, double>& color )
{
    const auto& [r, g, b, a] = color;
    return os << std::left
        <<   "R:" << std::setw( 10 ) << r
        << "\tG:" << std::setw( 10 ) << g
        << "\tB:" << std::setw( 10 ) << b
        << "\tA:" << std::setw( 10 ) << a
        << std::right;
}



